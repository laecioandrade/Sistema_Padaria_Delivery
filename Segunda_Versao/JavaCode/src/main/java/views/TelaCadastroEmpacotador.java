    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Embalador embalador = new Embalador();
        int ver = 0;
        try {
            if(nomeEmpacotador.getText().length()<3 || cpfEmpacotador.getText().length()<11 || cpfEmpacotador.getText().length()>11){
                ver = 1;
            }else{
                embalador.setNome(nomeEmpacotador.getText());
                embalador.setCpf(cpfEmpacotador.getText());
            }
        } catch (Exception e) {
            ver = 1;
            System.out.println("Erro: "+e);
        }
        if(ver == 0){
            int cont=0;
            if(padaria.getEmbaladores().size()!=0){
                int z;
                for(z=0;z<padaria.getEmbaladores().size();z++){
                    cont+=1;
                }
                if(padaria.getEmbaladores().get(z-1).getID() == cont){
                    cont+=1;
                }
            }
            embalador.setID(cont);
            embalador.setStatus(embalador.aguardando());
            padaria.getEmbaladores().add(embalador);
            JFrame frame = new JFrame ("Aviso!");
            JOptionPane.showMessageDialog (frame, "Cadastrado Com Sucesso!");
            TelaCadastroEmpacotador empacotador = new TelaCadastroEmpacotador(padaria);
            empacotador.setLocationRelativeTo(null);
            empacotador.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
            JOptionPane.showMessageDialog (frame, "Preencha os dados corretamente!");
            TelaCadastroEmpacotador empacotador = new TelaCadastroEmpacotador(padaria);
            empacotador.setLocationRelativeTo(null);
            empacotador.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_btCadastrarActionPerformed

