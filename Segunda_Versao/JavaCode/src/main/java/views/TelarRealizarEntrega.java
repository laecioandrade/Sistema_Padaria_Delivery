    private void btEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntregarActionPerformed
        int test = 0;
        String id = idPedido.getText();
        Entregador entre = new Entregador();
        Embalador embala = new Embalador();
        if(padaria.getPedidos()!=null){
            for(int i=0;i<padaria.getPedidos().size();i++){
                if(id.equals(String.valueOf(padaria.getPedidos().get(i).getID()))){
                    padaria.getPedidos().remove(i);
                    for(int p=0; p<padaria.getEmbaladores().size();p++){
                        padaria.getEmbaladores().get(i).setStatus(embala.aguardando());
                    }
                    for(int p=0; p<padaria.getEntregadores().size();p++){
                        padaria.getEntregadores().get(i).setStatus(entre.aguardando());
                    }
                    test=1;
                }
            }
        }
        if(test==1){
            JFrame frame = new JFrame ("Aviso!");
                JOptionPane.showMessageDialog(frame, "Entregue Com Sucesso!");
                
            TelarRealizarEntrega entrega = new TelarRealizarEntrega(padaria);
            entrega.setLocationRelativeTo(null);
            entrega.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
                JOptionPane.showMessageDialog(frame, "Id nÃ£o encontrado!");
                
            TelarRealizarEntrega entrega = new TelarRealizarEntrega(padaria);
            entrega.setLocationRelativeTo(null);
            entrega.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btEntregarActionPerformed

    
