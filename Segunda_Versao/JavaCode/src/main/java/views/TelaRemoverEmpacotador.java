    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        String id;
        id = idEmpacotador.getText();
        int idveri = 0;
        for(int i=0; i<padaria.getEmbaladores().size();i++){
            if(id.equals(String.valueOf(padaria.getEmbaladores().get(i).getID()))){
                padaria.getEmbaladores().remove(i);
                idveri = 1;
            }
        }
        if(idveri == 0){
            JFrame frame = new JFrame ("Aviso!");
                    JOptionPane.showMessageDialog (frame, "Id nÃ£o encontrado!");

            TelaRemoverEmpacotador embaladore = new TelaRemoverEmpacotador(padaria);
            embaladore.setLocationRelativeTo(null);
            embaladore.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
                    JOptionPane.showMessageDialog (frame, "Removido Com Sucesso!");

            TelaRemoverEmpacotador embaladore = new TelaRemoverEmpacotador(padaria);
            embaladore.setLocationRelativeTo(null);
            embaladore.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btRemoverActionPerformed

