    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        String id;
        id = idCliente.getText();
        int idveri = 0;
        for(int i=0; i<padaria.getClientes().size();i++){
            if(id.equals(String.valueOf(padaria.getClientes().get(i).getID()))){
                padaria.getClientes().remove(i);
                idveri = 1;
            }
        }
        if(idveri == 0){
            JFrame frame = new JFrame ("Aviso!");
                    JOptionPane.showMessageDialog (frame, "Id nÃ£o encontrado!");

            TelaRemoverCliente rmvcliente = new TelaRemoverCliente(padaria);
            rmvcliente.setLocationRelativeTo(null);
            rmvcliente.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
            JOptionPane.showMessageDialog (frame, "Removido Com Sucesso!");
            TelaRemoverCliente rmvcliente = new TelaRemoverCliente(padaria);
            rmvcliente.setLocationRelativeTo(null);
            rmvcliente.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btRemoverActionPerformed


