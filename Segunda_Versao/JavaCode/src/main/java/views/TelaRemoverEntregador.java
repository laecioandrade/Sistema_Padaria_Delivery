    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        String id;
        id = idEntregador.getText();
        int idveri = 0;
        for(int i=0; i<padaria.getEntregadores().size();i++){
            if(id.equals(String.valueOf(padaria.getEntregadores().get(i).getID()))){
                padaria.getEntregadores().remove(i);
                idveri = 1;
            }
        }
        if(idveri == 0){
            JFrame frame = new JFrame ("Aviso!");
                    JOptionPane.showMessageDialog (frame, "Id nÃ£o encontrado!");

            TelaRemoverEntregador entregador = new TelaRemoverEntregador(padaria);
            entregador.setLocationRelativeTo(null);
            entregador.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
                    JOptionPane.showMessageDialog (frame, "Removido Com Sucesso!");

            TelaRemoverEntregador entregador = new TelaRemoverEntregador(padaria);
            entregador.setLocationRelativeTo(null);
            entregador.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btRemoverActionPerformed


