    private void btEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntregarActionPerformed
        ControllerPedido contro = new ControllerPedido();
       boolean a = contro.RemoverPedido(textIdpedido.getText());
       if(a){
            JFrame frame = new JFrame ("Aviso!");
            JOptionPane.showMessageDialog (frame, "Entregue Com Sucesso!");
            TelaRealizarEntrega rentre = new TelaRealizarEntrega();
            rentre.setLocationRelativeTo(null);
            rentre.setVisible(true);
            dispose();
        }else{
            JFrame frame = new JFrame ("Aviso!");
            JOptionPane.showMessageDialog (frame, "Id não encontrado!");
            TelaRemoverCliente client = new TelaRemoverCliente();
            client.setLocationRelativeTo(null);
            client.setVisible(true);
            dispose(); 
        }
    }//GEN-LAST:event_btEntregarActionPerformed

